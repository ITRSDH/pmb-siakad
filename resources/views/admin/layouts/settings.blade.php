<script>
document.addEventListener('DOMContentLoaded', function() {
    const customTemplate = document.querySelector('.custom-template');
    const customToggle = document.querySelector('.custom-toggle');
    
    // Toggle theme customizer
    customToggle.addEventListener('click', function() {
        customTemplate.classList.toggle('open');
    });
    
    // Close when clicking outside
    document.addEventListener('click', function(e) {
        if (!customTemplate.contains(e.target)) {
            customTemplate.classList.remove('open');
        }
    });
    
    // Keyboard accessibility
    customToggle.addEventListener('keypress', function(e) {
        if (e.key === 'Enter' || e.key === ' ') {
            e.preventDefault();
            customTemplate.classList.toggle('open');
        }
    });
});
</script>
